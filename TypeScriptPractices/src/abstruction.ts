class person {
    name : string;
    skin : string;
    gender : string ;
    height : number;
    age : number;

    constructor(name: string , skin : string, gender: string, height : number, age : number){
        this.age = age;
        this.gender = gender;
        this.height = height;
        this.skin = skin;
        this.name = name;
    }
}